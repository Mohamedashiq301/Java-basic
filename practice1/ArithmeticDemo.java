package practice1;

public class ArithmeticDemo {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		int c=a+b;
		System.out.println(c);
		
		int d=15;
		int e=7;
		float f=d/e;
		float g=d%e;
		System.out.println(f);
		System.out.println(g);
		
		byte a1=10;
		short b1=20;
		int c1=a1+b1;
		System.out.println(c1);
		
		char a2=40;
		int b2=20;
		int c2=a2+b2;
		System.out.println(c2);
	}
}
